public class Array
{
    public static void main(String... args)
    {
        char   hello [ ] = {'H','e','l','l','o',',',' ','w','o','r','l','d','!'};

        short [] arr;       // declaration
        arr = new short[5]; // memory allocation

        arr [0] = 1;        // definition
        arr [1] = 2;
        arr [2] = 3;
        arr [3] = 4;
        arr [4] = 5;

        System.out.println(hello);



    }
}
